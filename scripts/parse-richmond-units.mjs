// One-time parser: richmond_units/unit_0*.md → supabase/migrations/057_richmond_tg5a_content.sql
// Real TG5A content (unit titles + per-lesson-group goals/vocabulary/language models) as idempotent
// upserts, so the data ships with migrations (deploys to prod) and is regenerable.
//   node scripts/parse-richmond-units.mjs   (re-run if the .md sources change)
import { readFileSync, writeFileSync, readdirSync } from 'node:fs'

const DIR = 'richmond_units'
const OUT = 'supabase/migrations/057_richmond_tg5a_content.sql'
const BOOK = 'TG5A'

const esc = (s) => s.replace(/'/g, "''")
const arr = (xs) =>
  xs.length ? `ARRAY[${xs.map((x) => `'${esc(x)}'`).join(', ')}]::text[]` : `'{}'::text[]`

// Split a lesson-group block into its ### subsections → { title: [body lines] }.
function subsections(block) {
  const map = {}
  for (const part of block.split(/^###\s+/m).slice(1)) {
    const lines = part.split('\n')
    map[lines[0].trim()] = lines
      .slice(1)
      .map((l) => l.trim())
      .filter((l) => l && l !== '---')
  }
  return map
}
const bullets = (lines = []) => lines.map((l) => l.replace(/^[-*]\s*/, '').trim()).filter(Boolean)

function parseUnit(md) {
  const unitNumber = Number(md.match(/^unit_number:\s*(\d+)/m)?.[1])
  const unitTitle = (md.match(/^unit_title:\s*(.+)$/m)?.[1] ?? '').trim()
  const groups = md
    .split(/^##\s+Lesson Group\s+/m)
    .slice(1)
    .map((block, i) => {
      const range = block.split('\n')[0].trim()
      const [start, end] = range.split('-').map(Number)
      const s = subsections(block)
      const vocabulary = (s['Vocabulary'] ?? [])
        .join(', ')
        // ponytail: placeholder keeps commas inside parentheses from splitting vocab items
        .replace(/\(([^)]*)\)/g, (_, inner) => '(' + inner.replace(/,/g, '\x00') + ')')
        .split(',')
        .map((t) => t.replace(/\x00/g, ',').trim())
        .filter(Boolean)
      return {
        range,
        start,
        end,
        sort_order: i + 1,
        learning_goals: bullets(s['Early Learning Goals']),
        vocabulary,
        language_models: bullets(s['Language Models']),
      }
    })
  return { unitNumber, unitTitle, groups }
}

const units = readdirSync(DIR)
  .filter((f) => /^unit_\d+\.md$/.test(f))
  .sort()
  .map((f) => parseUnit(readFileSync(`${DIR}/${f}`, 'utf8')))

// self-check
if (units.length !== 8) throw new Error(`expected 8 units, got ${units.length}`)
for (const u of units) {
  if (!u.unitTitle || !u.unitNumber) throw new Error(`unit ${u.unitNumber} missing title/number`)
  if (u.groups.length !== 4) throw new Error(`unit ${u.unitNumber} has ${u.groups.length} groups`)
  for (const g of u.groups)
    if (g.vocabulary.length === 0) throw new Error(`unit ${u.unitNumber} group ${g.range} no vocab`)
}

let sql = `-- AUTO-GENERATED by scripts/parse-richmond-units.mjs from richmond_units/*.md — do NOT edit by hand.
-- Real ${BOOK} content: 8 units × 4 lesson groups. Idempotent upserts (safe to re-apply).

insert into richmond_units (book_code, unit_number, unit_title) values
${units.map((u) => `  ('${BOOK}', ${u.unitNumber}, '${esc(u.unitTitle)}')`).join(',\n')}
on conflict (book_code, unit_number) do update set unit_title = excluded.unit_title;

`

for (const u of units) {
  sql += `-- Unit ${u.unitNumber}: ${u.unitTitle}\n`
  for (const g of u.groups) {
    sql += `insert into richmond_lesson_groups (unit_id, lesson_range, lesson_start, lesson_end, learning_goals, vocabulary, language_models, sort_order)
select u.id, '${g.range}', ${g.start}, ${g.end}, ${arr(g.learning_goals)}, ${arr(g.vocabulary)}, ${arr(g.language_models)}, ${g.sort_order}
from richmond_units u where u.book_code = '${BOOK}' and u.unit_number = ${u.unitNumber}
on conflict (unit_id, lesson_range) do update set
  lesson_start = excluded.lesson_start, lesson_end = excluded.lesson_end,
  learning_goals = excluded.learning_goals, vocabulary = excluded.vocabulary,
  language_models = excluded.language_models, sort_order = excluded.sort_order;\n\n`
  }
}

writeFileSync(OUT, sql)
console.log(`Wrote ${OUT}: ${units.length} units, ${units.reduce((n, u) => n + u.groups.length, 0)} lesson groups`)
