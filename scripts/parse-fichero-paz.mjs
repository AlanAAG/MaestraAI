// One-time parser: context/fichero_de_la_paz.md → lib/nem/fichero-paz.ts
// Extracts the PREESCOLAR fichas (número + nombre + texto de la actividad) from the SEP
// "Fichero para promover la cultura de paz". Run: node scripts/parse-fichero-paz.mjs
import { readFileSync, writeFileSync } from 'node:fs'

const SRC = 'context/fichero_de_la_paz.md'
const OUT = 'lib/nem/fichero-paz.ts'

const raw = readFileSync(SRC, 'utf8')

// ── 1. TOC: "Nombre de la ficha ......(presc) Ficha N" (two-column lines possible) ──
// Name may contain single periods ("Arriba las sonrisas. Convivir mejor"); the dot-leader is 4+.
const tocRe = /([^\n]{4,90}?)\s*\.{4,}[\s.]*\((presc|prim|sec)\)\s*Ficha\s+(\d+)/g
const toc = []
let m
while ((m = tocRe.exec(raw.slice(0, 20000))) !== null) {
  let nombre = m[1].trim()
  // Strip a leading ALL-CAPS section header glued onto the same line
  // (e.g. "PROMOVER LA IGUALDAD DE GÉNERO De canicas y muñecas").
  const words = nombre.split(/\s+/)
  let i = 0
  while (i < words.length - 1 && words[i] === words[i].toUpperCase() && /[A-ZÁÉÍÓÚÑ]{2,}/.test(words[i])) i++
  nombre = words.slice(i).join(' ')
  toc.push({ nombre, nivel: m[2], numero: Number(m[3]) })
}
const presc = toc.filter((f) => f.nivel === 'presc')
if (presc.length < 10) throw new Error(`TOC parse failed: only ${presc.length} presc fichas`)

// ── 2. Body pages: split on "---"; a ficha's pages start with its title ──
const pages = raw.split(/\n---\n/)
const norm = (s) =>
  s
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9 ]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()

const NOISE = [
  /^NIVEL TIEMPO ESTIMADO FICHA$/i,
  /^EDUCACI[ÓO]N PREESCOLAR/i,
  /^MINUTOS$/i,
  /^\d{1,3}$/, // page numbers / stray digits
  /^\d+\s+a\s+\d+\s+\d+$/i, // "30 a 40 1" (tiempo + nº ficha)
  /^Mantén la sana$/i,
  /^distancia, usa$/i,
  /^cubrebocas, saluda$/i,
  /^con la mano$/i,
  /^en el coraz[óo]n y$/i,
  /^l[áa]vate las manos$/i,
  /^constantemente\.?$/i,
  /^Pasos a seguir…?$/i,
  /^https?:\/\//i,
  /^Recuperado de:/i,
  /^Fecha de consulta/i,
  /^\d+ Adaptaci[óo]n de/i,
]

// Title may wrap across lines; join everything before the "NIVEL TIEMPO ESTIMADO" header line.
function pageTitle(page) {
  const lines = page.split(/\r?\n/).map((l) => l.trim())
  const head = []
  for (const l of lines) {
    if (/^NIVEL TIEMPO/i.test(l)) break
    if (l) head.push(l)
    if (head.length >= 3) break
  }
  return head.join(' ')
}

function titleMatches(nombre, page) {
  const a = norm(nombre)
  const b = norm(pageTitle(page))
  if (!b || b.length < 8) return false
  return a.startsWith(b) || b.startsWith(a)
}

function cleanPage(page, nombre) {
  const nName = norm(nombre)
  return page
    .split(/\r?\n/)
    .map((l) => l.trim())
    .filter((l) => {
      if (!l) return false
      const nl = norm(l)
      // repeated (possibly partial/wrapped) title lines
      if (nl.length >= 4 && (nName.startsWith(nl) || nl.startsWith(nName))) return false
      return !NOISE.some((re) => re.test(l))
    })
    .join('\n')
}

const fichas = []
for (const f of presc) {
  const body = pages
    .filter((p) => titleMatches(f.nombre, p))
    .map((p) => cleanPage(p, f.nombre))
    .join('\n\n')
  if (!body) {
    console.warn(`⚠ no body found for Ficha ${f.numero} "${f.nombre}" — skipped`)
    continue
  }
  // ponytail: cap texto at 4500 chars — enough for propósito + pasos; keeps the prompt block small.
  fichas.push({ numero: f.numero, nombre: f.nombre, texto: body.slice(0, 4500) })
}

fichas.sort((a, b) => a.numero - b.numero)

const header = `// AUTO-GENERATED by scripts/parse-fichero-paz.mjs — do not edit by hand.
// Source: context/fichero_de_la_paz.md (SEP, Fichero para promover la cultura de paz).
// Preescolar fichas only. Re-run the script if the source changes.

export interface FichaPaz {
  numero: number
  nombre: string
  texto: string
}

export const FICHAS_PAZ: FichaPaz[] = `

writeFileSync(OUT, header + JSON.stringify(fichas, null, 2) + '\n')
console.log(`✔ wrote ${fichas.length} preescolar fichas to ${OUT}`)
console.log(fichas.map((f) => `${f.numero}: ${f.nombre} (${f.texto.length} ch)`).join('\n'))
