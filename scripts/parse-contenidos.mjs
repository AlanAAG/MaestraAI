// One-time parser: context/Programa_sintetico_fase_2.md → lib/nem/contenidos-fase2.ts
// Extracts the 34 official Contenidos + their Tercer-grado (3°) PDAs, VERBATIM.
// Run: node scripts/parse-contenidos.mjs   (re-run if the source md changes)
import { readFileSync, writeFileSync } from 'node:fs'

const SRC = 'context/Programa_sintetico_fase_2.md'
const OUT = 'lib/nem/contenidos-fase2.ts'

const CAMPOS = [
  'Lenguajes',
  'Saberes y Pensamiento Científico',
  'Ética, Naturaleza y Sociedades',
  'De lo Humano y lo Comunitario',
]

const lines = readFileSync(SRC, 'utf8').split(/\r?\n/)
const out = []
let campo = null
let contenido = null
let grade = null // '1' | '2' | '3' | null

for (const raw of lines) {
  const line = raw.trim()
  const campoH = line.match(/^# (.+)$/)
  if (campoH && CAMPOS.includes(campoH[1])) {
    campo = campoH[1]
    contenido = null
    grade = null
    continue
  }
  const contH = line.match(/^### Contenido:\s*(.+)$/)
  if (contH) {
    contenido = { campo, contenido: contH[1].trim(), pdas3: [] }
    out.push(contenido)
    grade = null
    continue
  }
  const gradeH = line.match(/^\*\*([123])°\*\*$/)
  if (gradeH) {
    grade = gradeH[1]
    continue
  }
  // any other header ends the current 3° collection
  if (line.startsWith('#') || line.startsWith('**')) {
    grade = null
    continue
  }
  if (grade === '3' && contenido && line.startsWith('- ')) {
    contenido.pdas3.push(line.slice(2).trim())
  }
}

// self-check
const byCampo = {}
for (const c of out) byCampo[c.campo] = (byCampo[c.campo] ?? 0) + 1
if (out.length !== 34) throw new Error(`expected 34 contenidos, got ${out.length}`)
if (Object.keys(byCampo).length !== 4) throw new Error(`expected 4 campos, got ${Object.keys(byCampo)}`)
const empty = out.filter((c) => c.pdas3.length === 0)
if (empty.length) throw new Error(`contenidos with no 3° PDA: ${empty.map((c) => c.contenido).join(' | ')}`)

const header = `// AUTO-GENERATED by scripts/parse-contenidos.mjs — do NOT edit by hand.
// Verbatim official Contenidos + Tercer-grado (3°) PDAs, NEM Fase 2 (Programa Sintético).
// Source: context/Programa_sintetico_fase_2.md

export type ContenidoPDA = { campo: string; contenido: string; pdas3: string[] }

export const CONTENIDOS_FASE2_3: ContenidoPDA[] = `

writeFileSync(OUT, header + JSON.stringify(out, null, 2) + '\n')
console.log(`Wrote ${OUT}: ${out.length} contenidos`, byCampo)
