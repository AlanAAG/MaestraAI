// Normalize a word and look up its emoji. Handles articles, casing, and simple plurals.
export function wordToEmoji(word: string): string | null {
  if (!word) return null
  const w = word
    .toLowerCase()
    .trim()
    .replace(/^(a|an|the)\s+/, '')
    .replace(/[^a-z\s]/g, '')
    .trim()
  if (EMOJI[w]) return EMOJI[w]
  // simple plural вҶ’ singular
  if (w.endsWith('ies') && EMOJI[w.slice(0, -3) + 'y']) return EMOJI[w.slice(0, -3) + 'y']
  if (w.endsWith('es') && EMOJI[w.slice(0, -2)]) return EMOJI[w.slice(0, -2)]
  if (w.endsWith('s') && EMOJI[w.slice(0, -1)]) return EMOJI[w.slice(0, -1)]
  // first token of a phrase
  const first = w.split(/\s+/)[0]
  if (first && EMOJI[first]) return EMOJI[first]
  return null
}
